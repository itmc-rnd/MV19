/**
  * @brief This function handles TIM1 update interrupt and TIM10 global interrupt.
  */
void TIM1_UP_TIM10_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 0 */
	tm1t++;
	if(tm1t==100)
		tm1t = 0;
	
	if(tm1t<pwm)
		HAL_GPIO_WritePin(PWM_GPIO_Port, PWM_Pin, GPIO_PIN_SET);
	else
		HAL_GPIO_WritePin(PWM_GPIO_Port, PWM_Pin, GPIO_PIN_RESET);

  /* USER CODE END TIM1_UP_TIM10_IRQn 0 */
  HAL_TIM_IRQHandler(&htim1);
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 1 */

  /* USER CODE END TIM1_UP_TIM10_IRQn 1 */
}

/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
	tm2_speed++;
	if(tm2_speed==10)
	{
		speed = sw2*600;
		sw2 = 0;
		tm2_speed = 0;
	}
	sw1++;
	if (sw1==300)
		if (HAL_GPIO_ReadPin(GPIOG, GPIO_PIN_9))
			Config_request=true;
			//buzzer(1,1);
  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */

  /* USER CODE END TIM2_IRQn 1 */
}

/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */
	
		t3_counter++;
	if(t3_counter>duration_high+duration_low)
	{
		t3_counter = 0;
		spd = turbo_speed_high, led(8,1);
		is_inspiratory=1;
	}
	if(t3_counter==duration_low)
	{

		spd = turbo_spped_low, led(8,0);
		is_inspiratory=0;
	}
//	if((t3_counter%10)==0)
//	{	
		if(spd>=0)	
			turbo(spd);
	//}
		if(t3_counter%100)
		{
			led(6,0);
		}
		else
		{
		  led(6,1);
		}
  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */

  /* USER CODE END TIM3_IRQn 1 */
}

